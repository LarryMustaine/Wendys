package tools;

public class GenericObject {

	private String URL;
	private String Comment;
	
	public void setURL(String URL) {
		this.URL = URL;
	}
	
	public void setComment(String Comment) {
		this.Comment = Comment;
	}
	
	public String getURL() {
		return URL;
	}
	
	public String getComment() {
		return Comment;
	}
}